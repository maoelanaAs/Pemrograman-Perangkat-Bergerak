package com.learning.wordlistsqlwithcontentprovider;

public class WordItem {

    private int Id;
    private String Word;

    public WordItem() {
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getWord() {
        return Word;
    }

    public void setWord(String word) {
        Word = word;
    }
}
