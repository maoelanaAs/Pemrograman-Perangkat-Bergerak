package com.learning.databarang;

public class Barang {
    String kodeBrg, namaBrg;
    int hrgBeli, hrgJual, stokBrg;

    public Barang() {
    }

    public String getKodeBrg() {
        return kodeBrg;
    }

    public void setKodeBrg(String kodeBrg) {
        this.kodeBrg = kodeBrg;
    }

    public String getNamaBrg() {
        return namaBrg;
    }

    public void setNamaBrg(String namaBrg) {
        this.namaBrg = namaBrg;
    }

    public int getHrgBeli() {
        return hrgBeli;
    }

    public void setHrgBeli(int hrgBeli) {
        this.hrgBeli = hrgBeli;
    }

    public int getHrgJual() {
        return hrgJual;
    }

    public void setHrgJual(int hrgJual) {
        this.hrgJual = hrgJual;
    }

    public int getStokBrg() {
        return stokBrg;
    }

    public void setStokBrg(int stokBrg) {
        this.stokBrg = stokBrg;
    }
}
